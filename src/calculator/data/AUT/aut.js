const { alpha2ToAlpha3 } = require('i18n-iso-countries');

function getTable() {
  return [...document.getElementsByTagName('tr')].slice(1).map(tr => {
    const [nameEl, zoneEl] = [...tr.getElementsByTagName('td')];
    return [
      nameEl
        .getElementsByTagName('a')[0]
        .getAttribute('href')
        .slice(-7, -5),
      zoneEl.getElementsByTagName('span')[0].innerText,
    ];
  });
}

